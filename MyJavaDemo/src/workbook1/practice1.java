package workbook1;

public class practice1 {

	public static void main(String[] args) {
		//switch 
		int a = 2;
		switch(a){
			case 0:
				System.out.println("inside case0");
					break;
			case 1:
				System.out.println("inside case1");
					break;
			case 2:
				System.out.println("inside case2");
					break;
			case 3:
				System.out.println("inside case3");
					break;
			default:
				System.out.println("inside default block");
			
		}
		
	}

}
